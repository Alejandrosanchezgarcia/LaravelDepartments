    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate(['id' => 'required', 'nombre_emp' => 'required', 'email' => 'required', 'nombre_depart' => 'required']);

        Employee::create($request->all());

        return redirect()->route('employees.index')
            ->with('success', 'Empleado creado con éxito.');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Employee  $employee
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $obj = Employee::find($id);
        if ($obj === null) {
            return view('EmpNoExiste', ['id' => $id]);
        }
        return view('Emp', ['employee' => $obj]);
    }

